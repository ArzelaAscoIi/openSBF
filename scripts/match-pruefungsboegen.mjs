/**
 * Fetches the 15 official SBF See Prüfungsbögen from bootsfuehrerscheinpruefung.de,
 * extracts question numbers from __NEXT_DATA__, and prints the TypeScript constant
 * ready to paste into pruefungsboegen.ts.
 *
 * Run from project root: node scripts/match-pruefungsboegen.mjs
 */

async function fetchExamData(nummer) {
  const numStr = String(nummer).padStart(2, '0');
  const url = `https://www.bootsfuehrerscheinpruefung.de/sbfsee/pruefungsboegen/${numStr}`;
  const res = await fetch(url, {
    headers: { 'User-Agent': 'Mozilla/5.0 (compatible; openSBF-matcher/1.0)' },
  });
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  const html = await res.text();

  const m = html.match(/<script id="__NEXT_DATA__" type="application\/json">([\s\S]*?)<\/script>/);
  if (!m) throw new Error('No __NEXT_DATA__ found');

  const data = JSON.parse(m[1]);
  const questions = data?.props?.pageProps?.questions;
  if (!Array.isArray(questions)) throw new Error('No questions array in __NEXT_DATA__');

  return questions.map(q => ({
    number: q.number,
    question: q.question,
    correctAnswer: q.block?.options?.find(o => o.isCorrect)?.content ?? '',
  }));
}

const results = [];

for (let nummer = 1; nummer <= 15; nummer++) {
  const numStr = String(nummer).padStart(2, '0');
  process.stderr.write(`Fetching Prüfungsbogen ${numStr}...`);
  try {
    const questions = await fetchExamData(nummer);
    const questionIds = questions.map(q => q.number);
    console.error(` ${questionIds.length} questions: [${questionIds.join(', ')}]`);
    results.push({ nummer, questionIds });
  } catch (e) {
    console.error(` ERROR: ${e.message}`);
    results.push({ nummer, questionIds: [] });
  }
  await new Promise(r => setTimeout(r, 300));
}

// Output TypeScript constant
console.log(`// Auto-generated by scripts/match-pruefungsboegen.mjs`);
console.log(`// Official SBF See Prüfungsbögen — question numbers from the ELWIS catalog`);
console.log(`// (question number == app question ID == ELWIS catalog number)`);
console.log(``);
console.log(`import type { Pruefungsbogen } from '@/lib/types';`);
console.log(``);
console.log(`export const seePruefungsboegen: Pruefungsbogen[] = [`);

for (const { nummer, questionIds } of results) {
  const numStr = String(nummer).padStart(2, '0');
  console.log(`  { nummer: ${nummer}, questionIds: [${questionIds.join(', ')}] }, // Prüfungsbogen ${numStr}`);
}

console.log(`];`);
